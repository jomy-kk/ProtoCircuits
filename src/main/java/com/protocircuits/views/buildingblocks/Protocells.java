package com.protocircuits.views.buildingblocks;

public class Protocells {

    private String name;
    private String type;
    private String GC;
    private String toehold1;
    private String toehold2;
    private String output;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGC() {
        return GC;
    }

    public void setGC(String GC) {
        this.GC = GC;
    }

    public String getToehold1() {
        return toehold1;
    }

    public void setToehold1(String toehold1) {
        this.toehold1 = toehold1;
    }

    public String getToehold2() {
        return toehold2;
    }

    public void setToehold2(String toehold2) {
        this.toehold2 = toehold2;
    }

    public String getOutput() {
        return output;
    }

    public void setOutput(String output) {
        this.output = output;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
